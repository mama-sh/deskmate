import { createRequire } from "node:module";
import type { ChannelRoute, TeamConfig } from "@deskmate/core";
import { DEFAULT_SWEEP_CRON } from "@deskmate/core";

// Lazy accessor for core's front-desk prose. Importing it at module top-level made
// EVERY CLI command read core's `.md` at startup (this module is on the shared
// import graph); reading it only when `renderFrontDeskInstructions()` runs keeps
// `deskmate list`/`add`/etc. cheap. A synchronous `require` (of core's ESM export)
// keeps the renderers sync so `planSync` stays sync.
const require = createRequire(import.meta.url);

// Pure renderers for `deskmate sync`. Each returns the full text of one generated
// file, prefixed with the GENERATED banner so the consumer knows not to hand-edit
// it. No filesystem access here — these are string transforms over a TeamConfig,
// which keeps them trivially unit-testable (see test/render.test.ts).

/** The banner every generated CODE file starts with (see below for the .md/.env variants). */
export const BANNER = "// GENERATED by `deskmate sync` — edit deskmate.config.ts instead.";

/** `agent/agent.ts` — the front-desk root agent, running the team's default model. */
export function renderRootAgent(team: TeamConfig): string {
  return `${BANNER}
import { defineAgent } from "eve";

export default defineAgent({
  model: ${JSON.stringify(team.model)},
  // Keep @deskmate/core external so Eve traces it in the hosted output instead of
  // bundling it into each authored channel. Bundling it pulls core's transitive
  // deps (@vercel/connect, ai) into rolldown's per-channel graph and breaks the
  // build; a normally-installed core in node_modules is externalized anyway.
  build: { externalDependencies: ["@deskmate/core"] },
});
`;
}

/**
 * `agent/lib/deskmates.ts` — the roster registry core's channels consume. One
 * `DESKMATES` entry per deskmate, typed as core's `Roster`, with `providers` set
 * to the deskmate's `reads`. Values are JSON-encoded so any quoting/escaping in the
 * display strings stays correct.
 */
export function renderRosterRegistry(team: TeamConfig): string {
  const entries = Object.entries(team.deskmates).map(([id, d]) => {
    const identity = {
      id,
      displayName: d.displayName,
      emoji: d.emoji,
      summary: d.summary,
      providers: d.reads,
    };
    return `  ${id}: ${JSON.stringify(identity)},`;
  });
  return `${BANNER}
import type { Roster } from "@deskmate/core";

export const DESKMATES: Roster = {
${entries.join("\n")}
};
`;
}

/**
 * `agent/lib/channel-routes.ts` — the Slack channel→deskmate routing table core's
 * channels consume. One entry per `team.channels` mapping, typed as core's
 * `ChannelRoute`. Values are JSON-encoded so any quoting stays correct. Without this
 * file the routes in `deskmate.config.ts` would be validated but never applied.
 */
export function renderChannelRoutes(team: TeamConfig): string {
  const entries = Object.entries(team.channels).map(([channel, route]) => {
    const value: ChannelRoute = { deskmate: route.deskmate };
    if (route.lock !== undefined) value.lock = route.lock;
    if (route.watch !== undefined) value.watch = route.watch;
    return `  ${JSON.stringify(channel)}: ${JSON.stringify(value)},`;
  });
  return `${BANNER}
import type { ChannelRoute } from "@deskmate/core";

export const CHANNEL_ROUTES: Record<string, ChannelRoute> = {
${entries.join("\n")}
};
`;
}

/**
 * `agent/subagents/<id>/agent.ts` — a deskmate subagent shim. It re-derives the
 * agent config from the repo-root `deskmate.config.ts` (three levels up from
 * `agent/subagents/<id>/`) so the deskmate's description/model always tracks config.
 */
export function renderSubagentAgent(id: string): string {
  return `${BANNER}
import { defineDeskmate } from "@deskmate/core";
import team from "../../../deskmate.config.js";

export default defineDeskmate(team, ${JSON.stringify(id)});
`;
}

/**
 * Generic re-export shim. Re-exports the default export from `relImportPath`
 * (an eve tool/connection/channel authored elsewhere); with `{ star: true }` it
 * also re-exports the module's named bindings.
 */
export function renderReexport(relImportPath: string, opts?: { star?: boolean }): string {
  const lines = [BANNER, `export { default } from ${JSON.stringify(relImportPath)};`];
  if (opts?.star) lines.push(`export * from ${JSON.stringify(relImportPath)};`);
  return `${lines.join("\n")}\n`;
}

/**
 * `agent/channels/slack.ts` — build the managed Slack channel from the roster +
 * routes. The consumer's `team.frontDesk.maxTurns` is baked in as the convene cap
 * (env `DESKMATE_MAX_TURNS` still overrides it at runtime).
 */
export function renderSlackChannel(team: TeamConfig): string {
  return `${BANNER}
import { createSlackChannel } from "@deskmate/core/channels/slack";
import { DESKMATES } from "../lib/deskmates.js";
import { CHANNEL_ROUTES } from "../lib/channel-routes.js";

export default createSlackChannel(DESKMATES, CHANNEL_ROUTES, ${team.frontDesk.maxTurns});
`;
}

